package edunova.organizacijakoda;

public class Varijabla {
      static int j;
	//može se deklarirati na nivou klase
	
	public static void main(String[] args) {
    	   
    	//varijabla je prostor u memoriji
    	   
    	//primitivni tipovi podataka 
		//boolean,short,byte,int,long,float,double,char
		
		
    	 //deklaracija varijable
    	  
		 int i ;   //može se deklarirati unutar metode
		 
		 //dodjeljuivanje vrijednosti 
		 i= 7;
		 
		 //korištenje varijable
		 System.out.println(i);
		 
		 System.out.println(7);
		 
		 System.out.println(j);
		 
    	   
	
}
}
